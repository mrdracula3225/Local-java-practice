public class Operators {
    public static void main(String[] args) {
        /*
          Arithmetic operator
         int a = 9;
         int c = 10 + a;
         System.out.println(c);
        */
        /*
          module operator
        float a = 2;
        float b = 3 % a;
        System.out.println(b);
         */
        /*
        comparison operator
        System.out.println(64>5 && 64>10);
        System.out.println(64>5 || 64>100);
        */
    }
}
